function imc()
{
    let altura = prompt('Informe Sua altura');
    let peso = prompt('Informe Seu peso');
   
    let imc= peso / (altura*altura);

    alert(`Meu IMC é: ${imc}`);
    console.log(`Meu IMC é: ${imc}`);
}

