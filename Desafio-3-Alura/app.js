function fatorial()
{
    let numero = prompt('Informe um numero');

    for (var a = numero - 1; a >= 1; a--) 
    {
        numero = numero*a;
    }
    alert(`O fatorial deste numero é: ${numero}`);
    console.log(`O fatorial deste numero é: ${numero}`);
}

